# Selecting Parts of a String

name = "Jaylen"
print(name[0]) # Prints out J
print(name[1]) # Prints out a
print(name[2]) # Prints out y
print(name[3]) # Prints out l
print(name[4]) # Prints out e
print(name[5]) # Prints out n

# Selecting Parts of a String

fruit = "Pineapple"
print(fruit[:4]) # Prints out Pine
print(fruit[4:]) # Prints out apple

# Selection Parts of a String

text = "Randmon string with a lot of characters"
print(text[-1]) # Selects the last character of a string (s)
print(text[-2]) # Selects the 2nd to last character of a string (r)